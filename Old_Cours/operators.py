# operators you will be looking at
# + - * / % // ** =

x_position = 10

# addition
forward = x_position + 1
print(forward)

# subtraction
backward = x_position - 1
print(backward)

# modulo operation
remainder = 5 % 2
print(remainder)

# floor division
floor_division = 5 // 2
print(floor_division)

# exponentiation
five_squared = 5 ** 2
print(five_squared)

# combining arithmetic and assignment operators
# x_position = x_position + 1
x_position += 1
print(x_position)

# concatenating strings
first_name = "Nimish "
last_name = "Narang"
print(first_name + last_name)